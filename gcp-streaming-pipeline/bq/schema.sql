-- Usage:
--   bq query --use_legacy_sql=false < bq/schema.sql
--   OR paste into the BigQuery console.
--
-- Notes:
--   • The table is partitioned by DAY on the `timestamp` column so that
--     queries filtering on a date range only scan the relevant partitions
--     (cost savings + performance).
--   • A clustering key on (event, product) further prunes data for the
--     most common analytical query patterns.
--   • Partition expiry is set to 365 days to avoid unbounded storage costs.
--     Remove or adjust `partition_expiration_days` for production retention
--     requirements.
-- =============================================================================

-- Create the dataset (run once per environment) ---------------------------------
-- Replace `my_gcp_project` and `analytics` with your actual project / dataset.
